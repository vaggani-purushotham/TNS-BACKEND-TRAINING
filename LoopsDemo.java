// Demo for Loops
public class LoopsDemo {
    public static void main(String[] args) {

        // for loop

        for(int i=0;i<5;i++){
            System.err.println(i);
        }
        System.out.println();

        // while loop

        int i=1;
        while(i<=10){
            System.err.println(i+"");
            i++;
        }
        System.out.println();

        // do-while loop

        int j=1;
        do { 
           System.out.println(j+"");
            j++;
        }
        while(j<=5);
        }

    }
    

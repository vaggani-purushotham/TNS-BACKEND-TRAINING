// Demo for TypeCasting
public class TypeCastingDemo {
    public static void main(String[] args) {
        // Automatic Typecasting!
        int num=10;
        double d=num;
        System.err.println("conversion into double value is: "+d);
       // Manually TypeCasting!
       double value=464.00;
       int n=(int)value;
       System.err.println("After conversion the integer value is: "+n);
    }
}
